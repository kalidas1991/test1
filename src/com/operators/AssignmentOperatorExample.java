package com.operators;

public class AssignmentOperatorExample {
	public static void main(String[] args) {
		
		
		int a = 5;
		int b = 10;
		int x;
		x = a;
		System.out.println(x);
		x+=5;   // x = x + 5;
		System.out.println(x);
		x-=2;   // x = x - 2   ---- x = 10 -2
		System.out.println(x);
		x/=2;  // x = x/2 ;   ----- x = 8/2;
		System.out.println(x);
		
		
	}

}
