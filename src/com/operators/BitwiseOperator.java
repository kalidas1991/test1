package com.operators;

public class BitwiseOperator {
	
	public static void main(String[] args) {
		
		int x = 10;
		int y = 11;
		System.out.println("Bitwise X & Y value is : "+(x&y));
		
		
		int a = 11;
		int b = 10;
		System.out.println("Bitwise A | B value is : "+(a|b));
		
		
		int m = 11;
		int n = 10;
		System.out.println("Bitwise M ^ N value is : "+(m^n));
		
	}
}
